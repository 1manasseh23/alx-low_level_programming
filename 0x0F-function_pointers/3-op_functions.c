#include "3-calc.h"

/**
 * op_add - A functio that add a and b
 * @a: An input integer
 * @b: An input integer
 * Return: the sum a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A functio that subtract a by b
 * @a: An input integer
 * @b: An input integer
 * Return: the sum a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A functio that multiply a by b
 * @a: An input integer
 * @b: An input integer
 * Return: the sum a and b
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - A functio that divde a by b
 * @a: An input integer
 * @b: An input integer
 * Return: the sum a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - A functio that returns the remainder of the division of a by b
 * @a: An input integer
 * @b: An input integer
 * Return: the sum a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
